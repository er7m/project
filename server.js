var Grass = require("./Grass.js");
var XotaGishatich = require("./XotaGishatich.js");
var Xotaker = require("./Xotaker.js");
var Gish = require("./Gish.js");
var Amena = require("./Amena.js");

grassArr = [], XotakerArr = [], gishArr = [], amenaArr = [], xotagishatichArr = [];

for (var y = 0; y < matrix.length; y++) {
    for (var x = 0; x < matrix[y].length; x++) {

        if (matrix[y][x] == 1) {
            var gr = new Grass(x, y, 1);
            grassArr.push(gr);
        }
        if (matrix[y][x] == 2) {
            var xt = new Xotaker(x, y, 1);
            XotakerArr.push(xt);
        }
        if (matrix[y][x] == 3) {
            var gs = new Gish(x, y, 1);
            gishArr.push(gs);
        }
        if (matrix[y][x] == 4) {
            var am = new Amena(x, y, 1);
            amenaArr.push(am);
        }
        if (matrix[y][x] == 5) {
            var dr = new XotaGishatich(x, y, 1);
            xotagishatichArr.push(dr);
        }
    }
}


function drawServerayin() {

    for (var i in grassArr) {
        grassArr[i].mult()
    }
    for (var v in XotakerArr) {
        XotakerArr[v].move()
        XotakerArr[v].mult()
        XotakerArr[v].eat()
        XotakerArr[v].die()
    }
    for (var i in gishArr) {
        gishArr[i].move()
        gishArr[i].mult()
        gishArr[i].eat()
        gishArr[i].die()
    }
    for (var i in amenaArr) {
        amenaArr[i].eat()
    }
    for (var i in xotagishatichArr) {
        xotagishatichArr[i].utel()
    }
    io.sockets.emit("matrix", matrix)
}

