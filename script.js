var side = 15;

var grassArr = [];
var XotakerArr = [];
var gishArr = [];
var amenaArr = [];
var xotagishatichArr = [];
for (var y = 0; y < matrix.length; y++) {
    for (var x = 0; x < matrix[y].length; x++) {

        if (matrix[y][x] == 1) {
            var gr = new Grass(x, y, 1);
            grassArr.push(gr);
        }
        if (matrix[y][x] == 2) {
            var xt = new Xotaker(x, y, 1);
            XotakerArr.push(xt);
        }
        if (matrix[y][x] == 3) {
            var gs = new Gish(x, y, 1);
            gishArr.push(gs);
        }
        if (matrix[y][x] == 4) {
            var am = new Amena(x, y, 1);
            amenaArr.push(am);
        }
        if (matrix[y][x] == 5) {
            var am = new XotaGishatich(x, y, 1);
            xotagishatichArr.push(am);
        }
    }
}
function setup() {
    frameRate(4);
    background('#acacac');
    createCanvas(matrix[0].length * side, matrix.length * side);
}


function draw() {

    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
            }
            else if (matrix[y][x] == 3) {
                fill("red");
            }
            else if (matrix[y][x] == 4) {
                fill("#000000");
            }
            else if (matrix[y][x] == 5) {
                fill("#c81edb");
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
            }


            rect(x * side, y * side, side, side);

        }
    }

    for (var i in grassArr) {
        grassArr[i].mult()
    }
    for (var v in XotakerArr) {
        XotakerArr[v].move()
        XotakerArr[v].mult()
        XotakerArr[v].eat()
        XotakerArr[v].die()
    }
    for (var i in gishArr) {
        gishArr[i].move()
        gishArr[i].mult()
        gishArr[i].eat()
        gishArr[i].die()
    }
    for (var i in amenaArr) {
        amenaArr[i].eat()
    }
    for (var i in xotagishatichArr) {
        xotagishatichArr[i].utel()
    }
}

